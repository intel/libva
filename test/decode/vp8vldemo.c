static void download_yuv_to_file(VADisplay va_dpy, VASurfaceID surface_id)
{
    VAImage surface_image;
    VAStatus va_status;
    void *surface_p = NULL;
    unsigned char *y_src, *uv_src;
    unsigned char *y_dst, *uv_dst;
    unsigned char image[CLIP_WIDTH * CLIP_HEIGHT * 3 >> 1];
    int row, col;

#ifndef ANDROID
    FILE *fp = fopen("./1.yuv", "wb+");
#else
    FILE *fp = fopen("/sdcard/1.yuv", "wb+");
#endif
    va_status = vaDeriveImage(va_dpy, surface_id, &surface_image);
    CHECK_VASTATUS(va_status,"vaDeriveImage");

    vaMapBuffer(va_dpy, surface_image.buf, &surface_p);
    assert(VA_STATUS_SUCCESS == va_status);

    y_src = (unsigned char *)surface_p + surface_image.offsets[0];
    uv_src = (unsigned char *)surface_p + surface_image.offsets[1]; /* UV offset for NV12 */

    y_dst = image;
    uv_dst = y_dst + CLIP_WIDTH * CLIP_HEIGHT;

    /* Y plane */
    fprintf(stderr, "width is %d, height is %d, y_src is 0x%x, uv_src is 0x%x, pitch is %d.\n",
        surface_image.width, surface_image.height, y_src, uv_src, surface_image.pitches[0]);
    for (row = 0; row < CLIP_HEIGHT; row++) {
        memcpy(y_dst, y_src, surface_image.width);
        y_src += surface_image.pitches[0];
        y_dst += CLIP_WIDTH;
    }
    /* UV plane */
    for (row = 0; row < CLIP_HEIGHT/2; row++) {
        memcpy(uv_dst, uv_src, surface_image.width);
        uv_src += surface_image.pitches[0];
        uv_dst += CLIP_WIDTH;
    }

    fwrite(image, 1, sizeof(image), fp);

    vaUnmapBuffer(va_dpy, surface_image.buf);
    vaDestroyImage(va_dpy, surface_image.image_id);
    fclose(fp);

    return;
}

